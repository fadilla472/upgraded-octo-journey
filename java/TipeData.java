public class TipeData {
    public static void main(String[] args) {
        int angka;       
        double decimal;  
        char karakter;   
        boolean bool;   

        angka = 10;
        decimal = 3.14;
        karakter = 'A';
        bool = true;

        System.out.println("Tipe data integer: " + angka);
        System.out.println("Tipe data double: " + decimal);
        System.out.println("Tipe data karakter: " + karakter);
        System.out.println("Tipe data boolean: " + bool);
    }
}
